    public static String censurarNumero(String tarjeta) {
    int longitud = tarjeta.length();
    if (longitud <= 4) {
        return tarjeta;
    } else {
        String ultimosDigitos = tarjeta.substring(longitud - 4, longitud);
        String reemplazo = "";
        for (int i = 0; i < longitud - 4; i++) {
            reemplazo += "*";
        }
        return reemplazo + ultimosDigitos;
    }
}

